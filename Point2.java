public class Point2 {
private double x;
private double y; 


public Point2(double x, double y) {
this.x = x;
this.y = y;    
}
public double getX(){
    return this.x;
}
public double getY(){
    return this.y;
}
public void setX(double d){
    this.x = d;
}
public void setY(double s){
    this.y = s;
}
}